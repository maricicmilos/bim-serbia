            <h2>THE ANNUAL CONFERENCE OF BIM SERBIA ASSOCIATION - 08.11.2018.</h2>
            <p>The first annual conference of the BIM Serbia Association, which took place on November 8, 2018, at the Hyatt Regency Hotel in Belgrade, was attended by more than 135 participants from both the country and abroad.</p>
            <p>The conference brought together lecturers from Slovenia, Croatia, Hungary, the UAE, Bosnia and Herzegovina and Serbia. The special guest of the conference was Marina Karolj, a significant representative of building SMART organization of Russia and director of the company Concurator, as one of the individuals who contributed the most to the BIM standardization in Russian Federation.</p>
            <p>The conference was opened by the President of the Assembly of the BIM Srbija Association, Mr. Boban Djordjevic, and the attendees were welcomed by the doc. Dr Mihailo Jovanovic, Director of the Office for Information Technologies and eGovernment, Branislav Bandic, Assistant of the Provincial Secretary for Economy of the Autonomous Province of Vojvodina, prof. Dr Darko StefanoviÄ‡, Vice Dean for Science and International Cooperation at the Faculty of Technical Sciences in Novi Sad, and doc. Dr Budimir Sudimac, Vice-Dean for Finance and Economic Cooperation of the Faculty of Architecture in Belgrade.</p>
            <p>Mr. Veljko Janjic, President of BIM Serbia, presented at the conference the plans of the Association and the strategy of standardization of BIM in Serbia. The central session of the conference included a presentation by Ms. Marina Korolj on the BIM standardization process in Russia, followed by a round table discussion on the implementation of BIM in the region. The conference encompassed several very interesting presentations of BIM solutions and implementation of BIM on projects.</p>

                        <li>Within the company membership, the company is entitled to 5 representatives in the Association, or 5 members in the Assembly of the Association;</li>
                        <li>The member company has the right to use the logo of the Association in its presentations;</li>
                        <li>In lectures, conferences and other events organized by the Association, in addition to the sponsors’ roll-up, a roll-up with the logos of the member companies will be featured as well;</li>
                        <li>The logo and a brief description of the member companies will be featured on the website of the BIM Serbia Association in the section Members.</li>
                        <li>For all activities, events and conferences organized by the Association, special discounts are specified for member-companies.</li>
                    </ul>
                </li>
                <li>Membership of individuals</li>
                <li>Student membership</li>
            </ul>
            <p>All members of the Association will be given discounts for events organized by the Association.</p>

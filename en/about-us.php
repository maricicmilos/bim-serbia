            <h2>ASSOCIATION’S GOALS</h2>
            <ul>
                <li>Gathering of experts and enthusiasts in the field of information and technical sciences and technologies, related to BIM / VDC technology through profession or interest, to form a single community;</li>
                <li>Creation and maintenance of a unique BIM engineers and experts registry, as well as companies using BIM / VDC technology within the course of their business activities;</li>
                <li>Education and information of members and the general public about the BIM / VDC technology, its practical application, as well as on the news in the mentioned area.</li>
                <li>Organization of professional meetings, seminars, fairs, trainings, consultations, workshops and conferences on the topic of BIM / VDC technology;</li>
                <li>Exchange of knowledge and experience between the members of the Association, as well as between the Association itself and other professional associations and communities;</li>
                <li>Organization and assistance in the preparation of working groups, providing guidelines and recommendations for the profession.</li>
                <li>Giving examples of good practice in the field of BIM / VDC technology and improving the exchange of experiences among members.</li>
                <li>Representing the interests of the Serbian construction sector in the field of BIM / VDC technology at the international level.</li>
                <li>Participation in the preparation of guidelines and regulations in the fields of BIM / VDC technology.</li>
                <li>Promoting of BIM / VDC technology for the introduction of advanced technologies in the Republic of Serbia and the region through education and counseling in accordance with the Law;</li>
                <li>Encouraging the introduction and compliance of legal regulations governing comprehensive processes in the construction industry as well as the application of BIM / VDC technology in the Republic of Serbia and its alignment with relevant European and global standards;</li>
                <li>Cooperation with organizations and public services, institutions, associations and other organizations in the country and abroad dealing with the same or similar activities.</li>
            </ul>

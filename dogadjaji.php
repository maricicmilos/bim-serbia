            <h2>GODIŠNJA KONFERENCIJA UDRUŽENJA BIM SRBIJA - 08.11.2018.</h2>
            <p>Prvoj godišnjoj konferenciju Udruženja BIM Srbija koja je održana 8. novembra 2018. godine u Hotelu Hyatt Regency u Beogradu prisustvovalo je više od 135 učesnika iz zemlje i inostranstva.</p>
            <p>Konferencija je okupila predavače iz Slovenije, Hrvatske, Mađarske, UAE, Bosne i Hercegovine i Srbije, a specijalni gost konferencije bila je Marina Karolj, značajni predstavnik organizacije building SMART Rusije i direktorka kompanije Concurator koja je jedna od najzaslužnijih osoba za standardizaciju BIM-a u Ruskoj Federaciji.</p>
            <p>Konferenciju je otvorio Predsednik Skupštine Udruženja BIM Srbija, gospodin Boban Đorđević, a prisutne su pozdravili doc. dr Mihailo Jovanović, direktor Kancelarije za informacione tehnologije i eUpravu, Branislav Bandić, pomoćnik pokrainskog sekretara za privredu Vlade AP Vojvodine, prof. dr Darko Stefanović, prodekan za nauku i međunarodnu saradnju fakulteta tehničkih nauka u Novom Sadu, i doc. dr Budimir Sudimac prodekan za finansije i saradnju sa privredom arhitektonskog fakulteta u Beogradu.</p>
            <p>Predsednik Udruženja BIM Srbija gospodin Veljko Janjić predstavio je na konferenciji planove Udruženja i startegiju standardizacije BIM-a u Srbiji. Centralna sesija je konferencija obuhvatila je prezentaciju gospođe Marine Korolj na temu procesa standardizacije BIM-a u Rusiji nakon čega je održan i okrugli sto sa temom implementacije BIM-a u regionu. Na konferenciji je prezentovano više veoma interesantnih prezentacija BIM rešenja i implementacije BIM-a na projektima.</p>
